// penggunaan variabel
fun main() {
    // deklarasi dan inisialisasi dari variabel tipe dinamic
    var dynamicVar = 2
    println("contoh dynamic variabel = " + dynamicVar)

    // deklarasi dan inisialisasi dari variabel tipe static
    var staticVar: Char = 'A'
    println("contoh static variabel = " + staticVar)
}